
//Pedir para o usuário digitar um numero
var num1 = prompt("Digite o primeiro numero");
            
num1 = parseFloat(num1);//Convertento o número adquirido variável num1 de string para number com o parseFloat(num1)
            
//Pedir para o usuário digitar ouro numero
var num2 = prompt("Digite outro numero");
            
num2 = parseFloat(num2); //Convertento o número adquirido variável num2 de string para number com o parseFloat(num2)
            
//Mostrar a soma dos numeros digitados
alert("A soma dos numeros é de: "+(num1+num2)); //Para mostrar soma basta seguir a lógica do alert.
        