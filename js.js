// checagem da estação com o Switch

let mes = "maio";

switch(mes){
  case 'janeiro':
  case 'fevereiro':
  case 'março':
    console.log('Verão!')
    break;
  case 'abril':
  case 'maio':  
  case 'junho':  
    console.log('Outono!')
    break;
  case 'julho':
  case 'agosto':
  case 'setembro':
    console.log('inverno!')
    break;
  case 'outubro': 
  case 'novembro':
  case 'dezembro':
    console.log('Primavera!')
}

console.log()